public class DoreModel {

    private int mYear;
    private String mStartDore;
    private String mEndDore;
    private int mDaftar;

    public int getYear() {
        return mYear;
    }

    public void setYear(int year) {
        mYear = year;
    }

    public String getStartDore() {
        return mStartDore;
    }

    public void setStartDore(String startDore) {
        mStartDore = startDore;
    }

    public String getEndDore() {
        return mEndDore;
    }

    public void setEndDore(String endDore) {
        mEndDore = endDore;
    }

    public int getDaftar() {
        return mDaftar;
    }

    public void setDaftar(int daftar) {
        mDaftar = daftar;
    }
}
